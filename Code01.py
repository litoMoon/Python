# 变量命名规则--硬性规则
# 1.变量名由数字、字母、下划线组成，不能包含特殊符号
# 2.数字不能开头
# 3.变量名不能和python的“关键字”重复
# 4.python中变量名，是大小写敏感的，区分大小写
# 合法变量名：a1 非法变量名：1a，a*a
# 变量命名规则--软性规则
# 1.给变量命名时，尽量使用描述性的单词来表示，尽量通过名字来表示出变量的作用
# count:计数  personInfo:表示个人信息
# 2.使用多个单词命名时，命名有多种规范
# 1.驼峰命名，除了第一个单词首字母之外，后续每个单词的首字母都大写
# 2.蛇形命名，单词之间，使用下划线_来进行分隔
# person_info、total_count